export const buildSeoLandingMetadata = (page) => {
  const url = `${normalizedSiteUrl}/${page.slug}`
  const imageUrl = `${normalizedSiteUrl}/opengraph-image`

  return {
    title: page.metaTitle,
    description: page.description,
    alternates: {
      canonical: url,
    },
    openGraph: {
      type: 'website',
      locale: 'ru_RU',
      url,
      siteName: 'ArtistCRM',
      title: page.metaTitle,
      description: page.description,
      images: [
        {
          url: imageUrl,
          width: 1200,
          height: 630,
          alt: page.title,
        },
      ],
    },
    twitter: {
      card: 'summary_large_image',
      title: page.metaTitle,
      description: page.description,
      images: [imageUrl],
    },
    robots: {
      index: true,
      follow: true,
    },
  }
}
